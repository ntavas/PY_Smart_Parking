"""
=======================================================================
config.py - Ρυθμίσεις Ασφαλείας της Εφαρμογής
=======================================================================

ΤΙ ΚΑΝΕΙ ΑΥΤΟ ΤΟ ΑΡΧΕΙΟ:
    Κρατάει τις βασικές ρυθμίσεις ασφαλείας (security settings) για
    την εφαρμογή. Κυρίως χρησιμοποιείται για τα JWT tokens, δηλαδή
    τα "ψηφιακά εισιτήρια" που αποδεικνύουν ότι ένας χρήστης είναι
    συνδεδεμένος.

ΓΙΑΤΙ ΥΠΑΡΧΕΙ:
    Κεντρικό σημείο για να αλλάζουμε ρυθμίσεις χωρίς να ψάχνουμε
    μέσα σε δεκάδες αρχεία.

ΠΟΤΕ ΧΡΗΣΙΜΟΠΟΙΕΙΤΑΙ:
    - Στο security.py: για να δημιουργεί και να επαληθεύει JWT tokens
    - Στο deps.py: για να διαβάζει τo SECRET_KEY

ΣΥΝΕΡΓΑΖΕΤΑΙ ΜΕ:
    security.py, deps.py
=======================================================================
"""

import os  # Χρησιμοποιούμε το os για να διαβάσουμε μεταβλητές περιβάλλοντος

class Settings:
    """
    Κλάση που συγκεντρώνει όλες τις ρυθμίσεις της εφαρμογής.
    Μια "κλάση" (class) είναι σαν ένα καλούπι που ομαδοποιεί
    σχετικά δεδομένα μαζί.
    """

    # Το όνομα της εφαρμογής (εμφανίζεται στην τεκμηρίωση του API)
    PROJECT_NAME: str = "Smart Parking API"

    # Το πρόθεμα (prefix) για όλα τα URL του API μας
    # Π.χ. /api/users, /api/parking/spots
    API_V1_STR: str = "/api"

    # Το μυστικό κλειδί για την υπογραφή των JWT tokens.
    # ΣΗΜΑΝΤΙΚΟ: Στην παραγωγή (production), αυτό πρέπει να είναι
    # ένα μεγάλο τυχαίο κλειδί αποθηκευμένο σε μεταβλητή περιβάλλοντος.
    # Εδώ παίρνουμε την τιμή από το περιβάλλον (π.χ. docker-compose),
    # και αν δεν υπάρχει, χρησιμοποιούμε μια προεπιλεγμένη τιμή.
    SECRET_KEY: str = os.getenv("SECRET_KEY", "CHANGE_THIS_SECRET_KEY_IN_PRODUCTION")

    # Ο αλγόριθμος κρυπτογράφησης που χρησιμοποιούμε για τα tokens.
    # HS256 σημαίνει HMAC with SHA-256 - ένας ευρέως χρησιμοποιούμενος αλγόριθμος.
    ALGORITHM: str = "HS256"

    # Πόσο ώρες ισχύει ένα token πριν λήξει.
    # 60 λεπτά * 24 ώρες * 7 μέρες = 1 εβδομάδα
    # Δηλαδή ο χρήστης παραμένει συνδεδεμένος για μία εβδομάδα.
    ACCESS_TOKEN_EXPIRE_MINUTES: int = 60 * 24 * 7  # 1 εβδομάδα

# Δημιουργούμε ένα μοναδικό αντίγραφο (instance) των ρυθμίσεων
# που θα χρησιμοποιεί ολόκληρη η εφαρμογή.
settings = Settings()
